package example05.com;

public class Person 
{
	
	private int id;
	private String name;
	private int age;
	private long phono;
	
	private Myadhar card;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public long getPhono() {
		return phono;
	}
	public void setPhono(long phono) {
		this.phono = phono;
	}
	public Myadhar getCard() {
		return card;
	}
	public void setCard(Myadhar card) {
		this.card = card;
	}
	

}
